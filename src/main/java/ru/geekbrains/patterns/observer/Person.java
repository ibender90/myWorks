package ru.geekbrains.patterns.observer;

public class Person extends Subscriber{

    private int id;
    private String name;

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public void notificate(Document document) {
        System.out.println(this.name + " received notification: " + document.getBody());
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
